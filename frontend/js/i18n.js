// Current language
let currentLang = localStorage.getItem('kd_lang') || 'en';

function t(key) {
  return I18N[currentLang]?.[key] || I18N.en[key] || key;
}

function setLanguage(lang) {
  currentLang = lang;
  localStorage.setItem('kd_lang', lang);
  document.body.classList.toggle('lang-hi', lang === 'hi');
  // Re-render current page
  if (window.Router) Router.refresh();
}

function getLocalizedName(item) {
  if (currentLang === 'hi' && item.nameHi) return item.nameHi;
  return item.name;
}

function getLocalizedField(obj, field) {
  if (currentLang === 'hi') {
    const hiField = field + 'Hi';
    if (obj[hiField]) return obj[hiField];
  }
  return obj[field] || '';
}
